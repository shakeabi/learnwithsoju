#!/usr/bin/env node
/**
 * Regenerate extension/core/hanja-levels-data.js from the official
 * 한국어문회 grade list (via rycont/hanja-grade-dataset).
 *
 * Usage: node scripts/build-hanja-levels.js
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'extension/core/hanja-levels-data.js');
const CSV_URL = 'https://raw.githubusercontent.com/rycont/hanja-grade-dataset/main/hanja.csv';

/** Easiest-first ordering for collapsing sub-grades (7급Ⅱ vs 7급 → 7). */
const LEVEL_DIGIT = {
  '8급': 8,
  '7급Ⅱ': 7, '7급': 7,
  '6급Ⅱ': 6, '6급': 6,
  '5급Ⅱ': 5, '5급': 5,
  '4급Ⅱ': 4, '4급': 4,
  '3급Ⅱ': 3, '3급': 3,
  '2급': 2,
  '1급': 1,
  '특급Ⅱ': 0, '특급': 0,
};

function parseCsv(text) {
  const rows = [];
  let i = 0;
  const len = text.length;

  function readField() {
    if (i >= len) return null;
    if (text[i] === '"') {
      i += 1;
      let field = '';
      while (i < len) {
        if (text[i] === '"') {
          if (text[i + 1] === '"') {
            field += '"';
            i += 2;
          } else {
            i += 1;
            break;
          }
        } else {
          field += text[i++];
        }
      }
      if (text[i] === ',') i += 1;
      return field;
    }
    const start = i;
    while (i < len && text[i] !== ',' && text[i] !== '\n' && text[i] !== '\r') i += 1;
    const field = text.slice(start, i);
    if (text[i] === ',') i += 1;
    return field;
  }

  const headers = [];
  while (true) {
    const field = readField();
    if (field == null) break;
    headers.push(field);
    if (text[i - 1] !== ',' && (text[i] === '\n' || text[i] === '\r')) break;
  }
  if (text[i] === '\r') i += 1;
  if (text[i] === '\n') i += 1;

  while (i < len) {
    const record = {};
    for (let h = 0; h < headers.length; h += 1) {
      const field = readField();
      if (field == null) break;
      record[headers[h]] = field;
    }
    if (Object.keys(record).length) rows.push(record);
    while (i < len && (text[i] === '\r' || text[i] === '\n')) i += 1;
  }
  return rows;
}

async function main() {
  const res = await fetch(CSV_URL);
  if (!res.ok) throw new Error(`Failed to fetch ${CSV_URL}: ${res.status}`);
  const rows = parseCsv(await res.text());

  /** @type {Record<string, number>} */
  const final = {};
  for (const row of rows) {
    const ch = row.hanja;
    const lv = row.level;
    if (!ch || !(lv in LEVEL_DIGIT)) continue;
    const digit = LEVEL_DIGIT[lv];
    // Keep the easiest assigned grade when a character appears more than once.
    if (!(ch in final) || digit > final[ch]) final[ch] = digit;
  }

  const keys = Object.keys(final).sort();
  if (keys.length !== 5978) {
    console.warn(`warning: expected 5978 unique hanja, got ${keys.length}`);
  }

  const lines = keys.map((ch) => `  ${JSON.stringify(ch)}: ${final[ch]},`);
  const body = [
    '/**',
    ' * Auto-generated by scripts/build-hanja-levels.js — do not edit by hand.',
    ' * Source: https://github.com/rycont/hanja-grade-dataset (한국어문회 배정한자).',
    ' * Values: 8 = 8급 (easiest) … 1 = 1급, 0 = 특급 (hardest).',
    ' */',
    'export const HANJA_LEVEL_BY_CHAR = {',
    ...lines,
    '};',
    '',
  ].join('\n');

  fs.writeFileSync(OUT, body, 'utf8');
  console.log(`Wrote ${OUT} (${keys.length} entries, ${Buffer.byteLength(body)} bytes)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
